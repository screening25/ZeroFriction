/** Gemini parse system prompt builder. */
export function buildParsePrompt(text: string, today: string): string {
  return `오늘 날짜는 ${today}야. 사용자의 입력을 분석하여 다음 압축 JSON 포맷으로 설명 없이 오직 JSON만 반환해.
특히 'c' 필드는 입력의 의도나 단어에 따라 카테고리를 추론하여 한국어 또는 영어(WORK, PERSONAL, HEALTH 등)로 분류해. 재고는 "재고", 메모는 "메모"로 분류해.

[중요 한국어 정밀 파싱 규칙]
1. 시간/날짜 뒤에 붙는 조사('에', '은', '는', '이', '가', '을', '를')는 절대 제목("v" 필드)에 남겨두지 말고 완벽히 제거해야 해!
   - 입력: "19시에 퇴근" -> "v"는 "퇴근" (결코 "에 퇴근" 또는 "에퇴근"이 아님!), "attr.time"은 "19:00"
   - 입력: "오후 3시에 회의" -> "v"는 "회의", "attr.time"은 "15:00"
2. "21일", "25일"과 같이 숫자로 지정된 일자는 오늘 연도/월(오늘 날짜: ${today} 기준)을 차용하여 "attr.date"를 "YYYY-MM-DD" 형태로 강제 매핑하고, 제목에서 해당 일자와 조사를 반드시 지워야 해!
   - 입력: "21일에 출장" -> "v"는 "출장", "attr.date"는 "2026-05-21" (오늘이 2026년 5월인 경우)
   - 입력: "25일 미팅" -> "v"는 "미팅", "attr.date"는 "2026-05-25"

[입력 해석 가이드 및 Few-shot 예시]
1. 콤마와 영문이 혼용된 재고 입출고 예시:
- 입력: "FTG11-0001, S1 Vest 1 EA Inbound"
  출력: { "a":"C","t":"asset","c":"재고","v":"S1 Vest","attr":{"flow":"IN","qty":1,"code":"FTG11-0001"},"k":"" }
- 입력: "FTG11-0002, L1 Vest 10 EA Outbound"
  출력: { "a":"C","t":"asset","c":"재고","v":"L1 Vest","attr":{"flow":"OUT","qty":10,"code":"FTG11-0002"},"k":"" }

2. 한국어 재고 입출고 예시:
- 입력: "재고 티셔츠 5개 입고"
  출력: { "a":"C","t":"asset","c":"재고","v":"티셔츠","attr":{"flow":"IN","qty":5},"k":"" }

3. 일정 등록 예시 (시간, 조사, 날짜 정제 규칙 적용):
- 입력: "오늘 오후 3시 회의"
  출력: { "a":"C","t":"event","c":"WORK","v":"회의","attr":{"date":"2026-05-18","time":"15:00","category":"WORK"},"k":"" }
- 입력: "21일에 출장" (오늘이 2026년 5월 18일인 경우)
  출력: { "a":"C","t":"event","c":"WORK","v":"출장","attr":{"date":"2026-05-21","category":"WORK"},"k":"" }
- 입력: "19시에 퇴근"
  출력: { "a":"C","t":"event","c":"PERSONAL","v":"퇴근","attr":{"date":"2026-05-18","time":"19:00","category":"PERSONAL"},"k":"" }

[추가 액션 R (READ / 자연어 질의 NLQ)]
사용자가 "찾아", "검색", "보여줘", "현황", "리스트", "몇 개", "얼마나", "있나" 등 조회 의도를 표현하면 "a"를 "R"로 반환해라. R 액션은 데이터를 저장하지 않고 기존 records를 필터링하는 용도다.
- 입력: "이번주 재고 부족한 거 뭐 있어?" -> {"a":"R","t":"asset","c":"","v":"","attr":{},"rec":"none","link":[],"k":"부족"}
- 입력: "맥북 관련 일정 보여줘" -> {"a":"R","t":"event","c":"","v":"","attr":{},"rec":"none","link":[],"k":"맥북"}
- 입력: "메모 전체 검색" -> {"a":"R","t":"memo","c":"","v":"","attr":{},"rec":"none","link":[],"k":""}

[반복 일정 rec]
"매일/매주/매달/매월/날마다/주마다/매주 월요일" 등이 포함되면 rec 필드에 "daily"|"weekly"|"monthly" 중 하나를 넣어라. 없으면 "none".
- 입력: "매주 월요일 오전 10시 주간 스탠드업" -> rec: "weekly"
- 입력: "매일 아침 8시 러닝" -> rec: "daily"

[엔티티 링크 link]
입력에 "#태그" 또는 "@멘션" 또는 명시적 연관 키워드("관련", "연결")가 있으면 키워드들을 link 배열로 추출해라. 예: "맥북 발주 #프로젝트A 관련 회의" -> link: ["프로젝트A","맥북 발주"]. 일반 입력은 빈 배열 [].

JSON Schema:
{
  "a": "C" | "U" | "D" | "R" (C: 등록, U: 수정, D: 삭제, R: 검색/조회),
  "t": "event" | "asset" | "memo",
  "c": "카테고리명(자동 추론)",
  "v": "제목 또는 내용",
  "attr": {
    "flow": "IN" | "OUT",
    "qty": 수량 (숫자),
    "code": "코드",
    "loc": "위치",
    "mgr": "담당자",
    "demo": boolean,
    "date": "YYYY-MM-DD",
    "time": "HH:MM",
    "category": "분류(WORK, PERSONAL 등)",
    "memo": "상세 메모",
    "content": "메모 내용"
  },
  "rec": "none" | "daily" | "weekly" | "monthly",
  "link": ["연관 키워드 또는 해시태그 (없으면 빈 배열)"],
  "k": "수정/삭제/검색 시 대상 키워드"
}

입력: "${text}"`;
}
